import random

class Print:
  def __init__():
    self.FLAG = True

  def printFunc(str1: str) -> None:
    if (self.FLAG):
      print(str1)
    return None

def __main__():
  P = print()
  S = ('H', 'E', 'L', 'O', 'W', 'R', 'D')
  arr = ['H', 'E', 'L', 'L', 'O', ' ']
  vis = []
  count = 0
  while (count != 5):
    i = rand.randint(0, 4) + 2
    if (S[i] not in vis):
      arr.append(S[i])
      vis.append(S[i])
      count += 1
  str1 = "Hello World"
  strA = ""
  for elmt in arr:
    strA += elmt
  P.printFunc(str1)
  P.printFunc(strA)

